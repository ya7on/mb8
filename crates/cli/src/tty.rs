fn draw_glyph(framebuffer: &mut [u32], fb_width: usize, x: usize, y: usize, ch: usize, color: u32) {
    let glyph = FONT[ch];

    let fb_height = framebuffer.len() / fb_width;

    debug_assert!(
        x + 8 <= fb_width && y + 8 <= framebuffer.len() / fb_width,
        "Glyph out of bounds: x={}, y={}, fb={}x{}",
        x,
        y,
        fb_width,
        framebuffer.len() / fb_width
    );

    for (gy, row) in glyph.iter().enumerate() {
        let mut reverse_row = 0;
        for i in 0..8 {
            reverse_row |= ((row >> i) & 1) << (7 - i);
        }
        for gx in 0..8 {
            let bit = (reverse_row >> (7 - gx)) & 1;

            let px = x + gx;
            let py = y + gy;

            if px < fb_width && py < fb_height {
                framebuffer[py * fb_width + px] = if bit == 1 { color } else { PIXEL_OFF_COLOR }
            }
        }
    }
}

#[derive(Debug, Clone)]
#[must_use]
pub struct Tty {
    buffer: Vec<char>,
    pub head: usize,
    pub tail: usize,
    full: bool,
    pub cols: usize,
    pub rows: usize,
}

impl Tty {
    pub fn new(col: usize, row: usize, capacity: usize) -> Self {
        Self {
            buffer: vec![' '; capacity],
            head: 0,
            tail: 0,
            full: false,
            cols: col,
            rows: row,
        }
    }

    pub fn push_char(&mut self, c: char) {
        self.buffer[self.tail] = c;
        self.tail = (self.tail + 1) % self.buffer.len();

        if self.tail == self.head {
            self.head = (self.head + 1) % self.buffer.len();
        }
    }

    pub fn write_byte(&mut self, byte: u8) {
        let c = byte as char;

        match c {
            '\n' => self.move_to_next_line(),
            _ => self.push_char(c),
        }
    }

    fn move_to_next_line(&mut self) {
        let col = self.tail % self.cols;
        let advance = self.cols - col;

        self.tail = (self.tail + advance) % self.buffer.len();

        if self.tail == self.head {
            self.head = (self.head + advance) % self.buffer.len();
        }
    }

    pub fn get_visible(&mut self) -> Vec<char> {
        let visible_len = self.rows * self.cols;
        let mut out = Vec::with_capacity(visible_len);

        let total = if self.full {
            self.buffer.len()
        } else if self.tail >= self.head {
            self.tail - self.head
        } else {
            self.buffer.len() - self.head + self.tail
        };

        let start_offset = total.saturating_sub(visible_len);

        let mut idx = (self.head + start_offset) % self.buffer.len();

        for _ in 0..visible_len {
            out.push(self.buffer[idx]);
            idx = (idx + 1) % self.buffer.len();
        }

        out
    }

    pub fn render(&mut self, framebuffer: &mut [u32], fb_width: usize) {
        let fb_height = framebuffer.len() / fb_width;

        let max_rows = fb_height / 8;
        let max_cols = fb_width / 8;

        let chars = self.get_visible();

        let rows = self.rows.min(max_rows);
        let cols = self.cols.min(max_cols);

        for row in 0..rows {
            for col in 0..cols {
                let idx = row * cols + col;
                if idx >= chars.len() {
                    return;
                }

                let ch = chars[idx] as usize;
                let color = if ch == 0 {
                    PIXEL_OFF_COLOR
                } else {
                    PIXEL_ON_COLOR
                };

                draw_glyph(framebuffer, fb_width, col * 8, row * 8, ch, color);
            }
        }
    }

    pub fn load_from_slice(&mut self, src: &[u8]) {
        self.head = 0;
        self.tail = 0;
        self.full = false;

        for &b in src {
            self.push_char(b as char);
        }
    }

    pub fn reset_stream(&mut self) {
        self.head = 0;
        self.tail = 0;
        self.full = false;
    }

    pub fn clear(&mut self) {
        for ch in &mut self.buffer {
            *ch = ' ';
        }
        self.head = 0;
        self.tail = 0;
        self.full = false;
    }
}
